let valorA, valorB, valorC, valorD;

valorA = parseInt(prompt("Digite o primeiro valor: "));
valorB = parseInt(prompt("Digite o segundo valor: "));
valorC = parseInt(prompt("Digite o terceiro valor: "));
valorD = parseInt(prompt("Digite o último valor: "));

alert ("Multiplicação");
   alert (valorA + " * " + valorB + " = " + valorA * valorB);
   alert (valorA + " * " + valorC + " = " + valorA * valorC);
   alert (valorA + " * " + valorD + " = " + valorA * valorD);
   alert (valorB + " * " + valorC + " = " + valorB * valorC);
   alert (valorB + " * " + valorD + " = " + valorB * valorD);
   alert (valorC + " * " + valorD + " = " + valorC * valorD);

   alert("Soma");
   alert (valorA + " + " + valorB + " = " + valorA + valorB);
   alert (valorA + " + " + valorC + " = " + valorA + valorC);
   alert (valorA + " + " + valorD + " = " + valorA + valorD);
   alert (valorB + " + " + valorC + " = " + valorB + valorC);
   alert (valorB + " + " + valorD + " = " + valorB + valorD);
   alert (valorC + " + " + valorD + " = " + valorC + valorD);
